            <h3>What is Believer and how does it work?</h3>
            <p>Believer is a platform that connects local businesses, community developers and home builders with their customers and rewards them for promoting them on social media. After signing up and downloading the Believer app, customers can follow different businesses and earn points for completing their missions. These points can later be redeemed for rewards. </p>


            <h3>How much does it cost to use Believer?</h3>
            <p>Believer is absolutely free for customer users. There is a monthly fee for builders, community developers and merchant partners to use the platform. </p>


            <h3>How do I create an account?</h3>
            <p>Simply download the app from the Apple App Store and follow the directions. Or you can sign up at <a href="https://believer.io/signup">believer.io/signup</a></p>


            <h3>Can I access the app through my browser?</h3>
            <p>Because of our nifty social media integration, you can only access the Believer platform through the app. </p>


            <h3>Is Believer available on both Android and iPhone devices?</h3>
            <p>Currently, Believer is only available on iPhone devices. We are working towards launching an Android version. Stay tuned.</p> 


            <h3>Do I need to have a Facebook account to use Believer and earn points?</h3>
            <p>Yes, currently all missions will include Facebook sharing and so you’ll need a real Facebook account to earn points. </p>


            <h3>If I’m a business or brand leader, how do I signup on the platform?</h3>
